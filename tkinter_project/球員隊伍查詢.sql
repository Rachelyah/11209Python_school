--查詢球員姓名
SELECT 
	年份, 
	所屬球隊, 
	球員編號, 
	球員姓名, 
	出場數, 
	先發次數, 
	中繼次數, 
	勝場數, 
	敗場數, 
	救援成功, 
	中繼成功, 
	有效局數, 
	面對打者數, 
	被安打數, 
	被全壘打數, 
	保送數, 
	三振數, 
	自責分
FROM cpbl_pitchings
WHERE 球員姓名 LIKE '%豪%'
GROUP BY 年份, 
	所屬球隊, 
	球員編號, 
	球員姓名, 
	出場數, 
	先發次數, 
	中繼次數, 
	勝場數, 
	敗場數, 
	救援成功, 
	中繼成功, 
	有效局數, 
	面對打者數, 
	被安打數, 
	被全壘打數, 
	保送數, 
	三振數, 
	自責分;

